
let numeroDeAlunos = ["a", "b",, "d", "e"]
for (let contador = 0; contador < numeroDeAlunos.length; contador++) {

    if (contador == 0) {
        console.log("Zero")

    }else if (contador % 2 == 0) {
        console.log(`O numero ${contador} é par`)

    }else{
        console.log(`O numero ${contador} é impar`)
    }
}




